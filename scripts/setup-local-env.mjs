#!/usr/bin/env node
// Generate client/.env.local and server/.env.local from `supabase status -o env`.
//
// Rationale: running local Supabase rotates the anon / service-role keys on
// every fresh start. Copy-pasting them by hand is error-prone; this script
// keeps both .env.local files in sync with whatever the CLI reports.
//
// Idempotent: preserves user-managed values already present in server/.env.local
// so local dev can keep stable local-only config across Supabase restarts.

import { execFileSync } from "node:child_process";
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const REQUIRED_KEYS = ["API_URL", "ANON_KEY", "SERVICE_ROLE_KEY"];
const DEFAULT_LOCAL_ENCRYPTION_KEY =
	"local-dev-only-encryption-key-do-not-use-in-production";
const GENERATED_HEADER =
	"# Generated by scripts/setup-local-env.mjs - do not commit.";

export function parseSupabaseEnv(raw) {
	if (typeof raw !== "string") {
		throw new Error("parseSupabaseEnv expects a string");
	}

	const map = {};
	for (const line of raw.split(/\r?\n/)) {
		const trimmed = line.trim();
		if (!trimmed || trimmed.startsWith("#")) continue;

		const match = trimmed.match(/^([A-Z0-9_]+)\s*=\s*(.*)$/);
		if (!match) continue;

		const [, key, rawValue] = match;
		let value = rawValue.trim();
		if (
			(value.startsWith('"') && value.endsWith('"')) ||
			(value.startsWith("'") && value.endsWith("'"))
		) {
			value = value.slice(1, -1);
		}
		map[key] = value;
	}

	const missing = REQUIRED_KEYS.filter((key) => !map[key]);
	if (missing.length > 0) {
		throw new Error(
			`supabase status output is missing required keys: ${missing.join(", ")}`,
		);
	}

	return {
		apiUrl: map.API_URL,
		anonKey: map.ANON_KEY,
		serviceRoleKey: map.SERVICE_ROLE_KEY,
		jwtSecret: map.JWT_SECRET ?? null,
		dbUrl: map.DB_URL ?? null,
	};
}

export function buildClientEnv({ apiUrl, anonKey }) {
	return [
		GENERATED_HEADER,
		`VITE_SUPABASE_URL=${apiUrl}`,
		`VITE_SUPABASE_ANON_KEY=${anonKey}`,
		"VITE_SLACK_CALLBACK_URL=http://localhost:5173/",
		"",
	].join("\n");
}

function extractExistingServerValue(existingEnv, key) {
	if (!existingEnv) return null;
	const match = existingEnv.match(new RegExp(`^${key}=(.+)$`, "m"));
	return match ? match[1].trim() : null;
}

export function buildServerEnv({
	apiUrl,
	serviceRoleKey,
	encryptionKey,
	existingEnv,
	port = 3000,
	corsOrigin = "http://localhost:5173,http://127.0.0.1:5173",
}) {
	const resolvedEncryptionKey =
		extractExistingServerValue(existingEnv, "FIELD_ENCRYPTION_KEY") ??
		encryptionKey ??
		DEFAULT_LOCAL_ENCRYPTION_KEY;
	const preservedLocalAdminEmails = extractExistingServerValue(
		existingEnv,
		"LOCAL_ADMIN_EMAILS",
	);
	const preservedLocalAdminBootstrapFlag = extractExistingServerValue(
		existingEnv,
		"ENABLE_LOCAL_ADMIN_BOOTSTRAP",
	);

	return [
		GENERATED_HEADER,
		`SUPABASE_URL=${apiUrl}`,
		`SUPABASE_SERVICE_ROLE_KEY=${serviceRoleKey}`,
		`FIELD_ENCRYPTION_KEY=${resolvedEncryptionKey}`,
		`PORT=${port}`,
		`CORS_ORIGIN=${corsOrigin}`,
		...(preservedLocalAdminBootstrapFlag
			? [`ENABLE_LOCAL_ADMIN_BOOTSTRAP=${preservedLocalAdminBootstrapFlag}`]
			: []),
		...(preservedLocalAdminEmails
			? [`LOCAL_ADMIN_EMAILS=${preservedLocalAdminEmails}`]
			: []),
		"",
	].join("\n");
}

function readIfExists(path) {
	try {
		return readFileSync(path, "utf8");
	} catch {
		return null;
	}
}

export function writeEnvFiles({
	parsed,
	clientEnvPath,
	serverEnvPath,
	defaultEncryptionKey = DEFAULT_LOCAL_ENCRYPTION_KEY,
	existingServerEnv,
}) {
	mkdirSync(dirname(clientEnvPath), { recursive: true });
	mkdirSync(dirname(serverEnvPath), { recursive: true });

	const clientEnv = buildClientEnv({
		apiUrl: parsed.apiUrl,
		anonKey: parsed.anonKey,
	});

	const serverEnv = buildServerEnv({
		apiUrl: parsed.apiUrl,
		serviceRoleKey: parsed.serviceRoleKey,
		encryptionKey: defaultEncryptionKey,
		existingEnv: existingServerEnv ?? readIfExists(serverEnvPath) ?? undefined,
	});

	writeFileSync(clientEnvPath, clientEnv);
	writeFileSync(serverEnvPath, serverEnv);

	return { clientEnv, serverEnv };
}

function runCli() {
	const repoRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
	const clientEnvPath = resolve(repoRoot, "client/.env.local");
	const serverEnvPath = resolve(repoRoot, "server/.env.local");

	let raw;
	try {
		raw = execFileSync("supabase", ["status", "-o", "env"], {
			cwd: repoRoot,
			encoding: "utf8",
			stdio: ["ignore", "pipe", "pipe"],
		});
	} catch (error) {
		const stderr =
			error && typeof error === "object" && "stderr" in error
				? String(error.stderr ?? "")
				: "";
		const message = [
			"Failed to run `supabase status -o env`.",
			"Is the Supabase local stack running? Try `pnpm supabase:start` first.",
			stderr && `\nsupabase CLI said:\n${stderr.trim()}`,
		]
			.filter(Boolean)
			.join(" ");
		console.error(message);
		process.exit(1);
	}

	const parsed = parseSupabaseEnv(raw);
	writeEnvFiles({ parsed, clientEnvPath, serverEnvPath });

	const clientExisted = existsSync(clientEnvPath);
	const serverExisted = existsSync(serverEnvPath);
	console.log(
		`${clientExisted ? "updated" : "wrote"} ${clientEnvPath}\n` +
			`${serverExisted ? "updated" : "wrote"} ${serverEnvPath}`,
	);
}

const invokedDirectly =
	import.meta.url === `file://${process.argv[1]}` ||
	import.meta.url.endsWith(process.argv[1] ?? "");

if (invokedDirectly) {
	runCli();
}
